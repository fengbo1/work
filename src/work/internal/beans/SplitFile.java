package work.internal.beans;
/*拆分文件名*/
public class SplitFile{
   private 	String type;
   private String date;
   private String pool;
   
   public SplitFile(){}
   
public void setType(String type) {
	this.type = type;
}
public String getType() {
	return type;
}
public void setDate(String date) {
	this.date = date;
}
public String getDate() {
	return date;
}
public void setPool(String pool) {
	this.pool = pool;
}
public String getPool() {
	return pool;
}
   
}